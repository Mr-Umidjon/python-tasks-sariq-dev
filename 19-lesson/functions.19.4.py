def solishtr(a, b):
    """Ikki sonni solishtiruvchi funksiya """
    if a > b:
        print(f"{a} > {b}")
    elif a < b:
        print(f"{a} < {b}")
    else:
        print(f"{a} = {b}")
        
solishtr(121,12)
solishtr(2, 2343)
solishtr(21,21)
solishtr(21**2, 3**3)
solishtr(12-2, 23*3)



